package com.packages.bitcamp.generics;

public class Book {

	private String name;
	private String autor;
	private int pages;
	private int numOfChars;
	
	
	public Book(String name, String autor, int page, int numOfChars) {
	  this.name = name;
	  this.autor = autor;
	  this.pages = page;
	  this.numOfChars = numOfChars;
	}

}
